// -------------------------------- ACCESSORS ------------------------------- //
// License text of the program accessor
/// \brief          License text of the program accessor.
/// \details        Gets the value of the text property with correctly replaced
///                 keywords.
/// \return         Text.
inline std::string AboutLicense::text(const std::string& program, const std::string& date, const std::string& owner, const std::string& contact, const std::string& organization, const std::string& purpose) const
{
    static const std::array<std::string, 6> tags({{"<program>", "<date>", "<owner>", "<contact>", "<organization>", "<purpose>"}});
    static const unsigned int npos = static_cast<unsigned int>(std::string::npos);
    const std::array<std::string, 6> values({{program, date, owner, contact, organization, purpose}});
    unsigned int marker = 0;
    std::string output = std::get<6>(_data);
    for (unsigned int ifield = 0; ifield < 6; ++ifield) {
        marker = 0;
        while((marker = output.find(tags[ifield], marker)) != npos) {
            output.replace(marker, tags[ifield].length(), values[ifield]);
            marker += values[ifield].size();
        }
    }
    return output;
}
